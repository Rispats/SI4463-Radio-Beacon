void send_cmd(uint8_t* d, uint8_t l) {
  digitalWrite(CSN, LOW);
  for (uint8_t i = 0; i < l; i++) SPI.transfer(d[i]);
  digitalWrite(CSN, HIGH);
}

bool cts() {
  for (uint8_t i = 0; i < 100; i++) {
    digitalWrite(CSN, LOW);
    SPI.transfer(0x44);
    delayMicroseconds(20);
    uint8_t c = SPI.transfer(0x00);
    digitalWrite(CSN, HIGH);
    if (c == 0xFF) return true;
    delay(5);
  }
  return false;
}

void init_radio() {
  digitalWrite(SDN, HIGH); delay(50);
  digitalWrite(SDN, LOW); delay(50);
  digitalWrite(CSN, HIGH);
  for (uint16_t i = 0; i < sizeof(rad_cfg); ) {
    uint8_t l = rad_cfg[i++];
    if (l == 0x00) break;
    send_cmd(&rad_cfg[i], l);
    i += l;
    cts();
  }
}

// Add checksum to message
char* add_cs(const char* msg) {
  static char t0[128]; //t0 is variable to store msg with checksum
  unsigned char cs = 0;
  int pl_len = strlen(msg);
  for (int i = 0; i < pl_len; i++) cs += msg[i]; 
  sprintf(t0, "%s%02X", msg, cs);
  return t0;
}

void send(char* msg) {
  uint8_t buf[64];
  uint8_t pl_len = strlen(msg);
  uint8_t total = 3 + pl_len; // start_bit + tx_id + msg + stop_bit

  buf[0] = 0x66; // WRITE_TX_FIFO
  buf[1] = START;
  buf[2] = ID;
  memcpy(&buf[3], msg, pl_len);
  buf[3 + pl_len] = STOP;

  send_cmd(buf, total + 1);
  cts();

  uint8_t tx[] = { 0x31, 0x00, 0x00, 0x00, total, 0x00, 0x00 };
  send_cmd(tx, sizeof(tx));
  cts();

  Serial.println(msg);
  Serial.println(" ");

}

void setup() {
  pinMode(SDN, OUTPUT);
  pinMode(CSN, OUTPUT);
  digitalWrite(CSN, HIGH);
  Serial.begin(115200);
  SPI.begin();
  SPI.beginTransaction(SPISettings(8000000, MSBFIRST, SPI_MODE0));
  init_radio();
  Serial.println("TX Ready");
}

